    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JPassWordFieldHint in_passWord = new JPassWordFieldHint(
                new javax.swing.JPasswordField(), "padlock", "Informe a senha");
        JOptionPane.showMessageDialog(null, in_passWord, "Validação para acesso", JOptionPane.PLAIN_MESSAGE);
        
        char[] senha = in_passWord.getPassword();
        if(senha.length == 0) return;
        int count = -1;
        
        try {
            for(String aux: new SenhaGerenteDAO().senhas()){
                char[] teste = aux.toCharArray();
                if(teste.length == senha.length) {
                    count = 0;
                    for(int i=0 ; i<senha.length ; i++) if(senha[i] == teste[i]) count++;
                    if(count == senha.length) liberaTelaCadastro(TipoUsuario.USUARIO_GERENTE);
                    else count = -1;
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Ocorreu algum erro no banco de dados");
        }
        if(count == -1){
            JOptionPane.showMessageDialog(null, "Senha inválida");
            dispose();
        }
            
    }//GEN-LAST:event_jButton2ActionPerformed
    
