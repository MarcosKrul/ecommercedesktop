    public void liberaTelaCadastro(TipoUsuario tipo) {
        TelaCadastroUsuario tela = new TelaCadastroUsuario(tipo);
        desktop.add(tela);
        tela.setVisible(true);
        tela.setPosicao();
        dispose();
    }
    
