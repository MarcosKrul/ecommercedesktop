    private void suporteCarregaDados() {
        List<Endereco> list = null;
        try {
            list = new EnderecoDAO().listaEnderecos(false, id_usuario);
        } catch (SQLException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Ocorreu algum erro no banco de dados");
        }
        if(list != null) modelo.setDados(list);
    }
    
