    private void btn_finalizar_endereco_proximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_finalizar_endereco_proximoActionPerformed
        int index = tabela_finalizar_endereco.getSelectedRow();
        if(index == -1){
            JOptionPane.showMessageDialog(null, "Por favor, selecione um endereço");
            return;
        }
        
        TelaFinalziarCompraConfirmarDados tela = new TelaFinalziarCompraConfirmarDados(id_usuario, modelo.getEnderecoIndex(index), desktop);
        desktop.add(tela);
        tela.setVisible(true);
        tela.setPosicao();
        dispose();
    }//GEN-LAST:event_btn_finalizar_endereco_proximoActionPerformed

